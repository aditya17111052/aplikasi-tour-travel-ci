								<h3 class="about-title mb-30">Keistimewaan ibadah umroh</h3>
								<p> Meski bukan yang utama, ibadah umroh tak kalah istimewanya dengan haji. Agar lebih memahami, berikut Dream berikan ulasan mengenai 6 keistimewaan ibadah umroh.<br/>
									1. Ibadah umroh yang satu kepada umroh berikutnya adalah Kaffaroh atau penghapus dosa<br/>
										Allah SWT akan menghapus dosa-dosa kita di antara pelaksanaan umroh yang satu dengan umroh berikutnya. Sederhananya begini, bila 5 atau 10 tahun lalu kita sudah melaksanakan umroh, tahun berikutnya berkesempatan untuk melakukan umroh, maka dosa-dosa di antara kedua waktu umroh tersebut akan diampuni Allah SWT.<br/><br/>
									2. Umroh adalah jihad bagi para wanita dan orang yang lemah<br/>
										Para ibu dan kaum wanita umumnya serta mereka yang lemah fisik pada masa Rasulullah SAW, tak bisa ikut berjihad atau berperang. Namun mereka masih mendapatkan fadillah pahala jihad dengan cara melaksanakan ibadah umroh.<br/><br/>
									3. Jemaah umroh adalah tamu-tamu Allah yang doanya dikabulkan<br/>
										Ibadah haji dan umroh menjadi istimewa karena orang yang datang ke Tanah Suci untuk menunaikan ibadah tersebut adalah tamu Allah SWT. Begitulah biasa disebut dan memang Rasulullah SAW menyebutkannya demikian. Jemaah haji dan umroh adalah tamu undangan Allah SWT sehingga apa yang diminta sang tamu akan dikabulkan oleh-Nya. Ini adalah bentuk keistimewaan yang paling istimewa.<br/><br/>
									4. Wafat saat menjalankan ibadah umroh pahalanya dicatat sampai hari kiamat<br/>
										Keutamaan orang-orang yang wafat dalam perjalanan untuk melaksanakan ibadah haji dan umroh, serta keutamaan orang yang wafat dalam keadaan berihram (di tengah pelaksanaan ibadah haji dan umroh) adalah mendapatkan anugerah pahalanya dicatat sampai hari kiamat. Semuanya termaktub jelas dalam hadits.<br/><br/>
									5. Ibadah umroh bisa menghilangkan kefakiran<br/>
										Bila kita melihat saudara kita yang setelah pulang dari haji atau umroh maka terlihat kehidupannya semakin baik, rezekinya mengalir terus dan keluarganya bertambah berkah, itulah salah satu fadillah ibadah tersebut. Seluruh biaya yang digunakan untuk berangkat haji dan umroh akan diganti oleh Allah SWT dengan berlipat-lipat.<br/><br/>
									6. Fadillah pahala satu kali umroh dengan sholat dua rakaat di Masjid Quba<br/>
										Sholat di Masjid Quba memiliki keutamaan tersendiri. Menurut Hadits Nabi SAW yang diriwayatkan oleh Abu bin Sahl bin Hunaif RA, ia pernah mendengar Rasulullah SAW bersabda: " Barangsiapa bersuci di rumahnya, kemudian mendatangi Masjid Quba, lalu ia sholat di dalamnya, maka baginya pahala seperti pahala umroh." (HR. Tirmizi no. 298, Ibnu Majah no. 1401)<br/>
										Masjid Quba ini terletak di luar kota Madinah. Jadi, mengingat fadillah sholat di Masjid Quba yang sangat besar, maka bila kita sudah berada di Madinah dalam rangka menjalankan ibadah umroh atau haji, jangan sampai lupa melakukan sholat di Masjid Quba.<br/>


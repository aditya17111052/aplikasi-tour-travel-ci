								<h3 class="about-title mb-30">Tata cara pelaksanaan umroh</h3>
								<p> Tata cara pelaksanaan umroh dimulai dengan membaca niat dan memakai pakaian ihram dari miqat-miqat yang telah ditentukan. Miqat adalah garis start seorang jamaah yang hendak melakukan ibadah umroh.
									Dengan kata lain miqat adalah tempat berihram (niat umroh) dan masuknya seseorang ke dalam pelaksanaan umroh yang akan dilakukan.<br/>
									1. Dari bandara menuju miqat Masjid Dzulhulaifah atau lebih dikenal Abyar 'Ali.<br/>
									   Di miqat yang terletak di Madinah ini, para jamaah melakukan persiapan sebelum ihram, mulai dengan mandi, mengenakan pakaian ihram, berwudhu dan mengerjakan sholat sunnah ihram 2 rakaat.
									   Setelah itu niat mengerjakan ibadah umroh dengan membaca bacaan niat umroh yaitu Labbaikallahumma 'umratan. Yang artinya 'Aku sambut panggilanMu ya Allah untuk menjalankan umroh'.<br/><br/>
									2. Setelah mengenakan pakaian ihram, seorang jamaah umroh dilarang untuk melakukan hal-hal yang sudah ditentukan syariat.<br/>
									   Bagi pria, dilarang:<br/>
										•	memakai pakaian biasa<br/>
										•	memakai alas kaki yang menutupi mata kaki<br/>
										•	menutup kepala dengan peci, topi, dan sebagainya<br/>
									   Bagi wanita, dilarang:<br/>
										•	memakai kaos tangan<br/>
										•	menutup muka<br/>
									   Bagi pria dan wanita, dilarang:<br/>
										•	memakai wangi-wangian<br/>
										•	memotong kuku, mencukur atau mencabut rambut/bulu<br/>
										•	memburu atau mematikan binatang apa pun<br/>
										•	menikah, menikahkan atau meminang wanita untuk dinikahi<br/>
										•	bermesraan atau berhubungan intim<br/>
										•	mencaci, bertengkar atau mengeluarkan kata-kata kotor<br/>
										•	memotong tanaman di sekitar Mekah<br/><br/>
									3. Menuju Masjidil Haram di Mekah</br>
										Dalam perjalanan, memperbanyak bacaan kalimat talbiyah yang selalu diucapkan Rasulullah shallallahu ‘alaihi wa sallam ketika umroh dan haji.</br>
										<b>LABBAIK ALLAHUMMA LABBAIK. LABBAIK LAA SYARIKA LAKA LABBAIK. INNAL HAMDA WAN NI’MATA LAKA WAL MULK LAA SYARIKA LAK</b><br/>
										Artinya:<br/>
											" Aku penuhi panggilan-Mu, ya Allah, aku penuhi panggilan-Mu. Tidak ada sekutu bagi-Mu, aku penuhi panggilan-Mu. Sesungguhnya segala puji, nikmat dan kerajaan bagi-Mu. Tidak ada sekutu bagi-Mu"<br/><br/> 
										Akhir waktu membaca talbiyah untuk umroh adalah saat akan memulai thawaf.<br/><br/>
									4. Melakukan thawaf.<br/>
										Sebelum masuk Masjidil Haram, jamaah dianjurkan berwudhu terlebih dahulu. Jamaah boleh masuk Masjidil Haram lewat pintu mana saja, tapi dianjurkan mengikuti contoh Rasulullah SAW yang masuk melalui pintu Babus Salam atau Bani Syaibah.
										Saat masuk Masjidil Haram, disarankan untuk mengucap doa <b>BISMILLAH WASH SHOLATU WAS SALAMU ‘ALA RASULULLAH. ALLAHUMMAFTAHLI ABWABA RAHMATIKA</b><br/>
										Artinya: <i>Dengan nama Allah, shalawat dan salam untuk Rasulullah. Ya Allah bukakanlah untukku pintu-pintu rahmat-Mu.</i><br/>
										Setelah itu turun dan terus menuju tempat thawaf (mataf). Jamaah mulai thawaf dari garis lurus (area dekat Hajar Aswad) antara pintu Kabah dan tanda lampu hijau di lantai atas Masjidil Haram.<br/>
										Di sini jamaah diberi pilihan antara lain:<br/>
										•	Taqbil yaitu mencium Hajar Aswad<br/>
										•	Istilam dan Taqbil yaitu mengusap, meraba, dan mencium Hajar Aswad<br/>
										•	Istilam yaitu mengusap Hajar Aswad dengan tangan atau sesuatu benda yang kita pegang, kemudian benda tersebut dicium, atau<br/>
										•	Melambaikan tangan atau benda yang kita pegang 3 kali, tidak dicium tapi mengucapkan <b>Bismillah, Allahu Akbar</b> (Dengan nama Allah, Allah Maha Besar)<br/>
										Salah satu pilihan ritual ini dilakukan setiap kali melewati Hajar Aswad dan Rukun Yamani pada putaran satu sampai tujuh. Jika tidak mampu mencium Hajar Aswad dan Rukun Yamani karena alasan keamanan akibat banyaknya jamaah yang umroh, maka bisa memilih istilam dengan tangan atau benda, atau hanya melambaikan tangan atau benda yang kita pegang.<br/>
										Pada putaran 1-3 jamaah pria dianjurkan untuk lari-lari kecil. Sedangkan pada putaran 4-7 dengan jalan biasa. Sementara untuk tata cara umroh wanita tidak ada lari-lari kecil saat melakukan thawaf.<br/>
										Sepanjang thawaf, membaca doa saat berada di antara Rukun Yamani dan Hajar Aswad. Doa saat thawaf yang selalu dibaca oleh Rasulullah SAW adalah doa sapu jagad, yaitu:<br/>
											<b>RABBANA ATINA FIDDUNYA HASANATAN WA FIL AKHIRATI HASANATA WA QINA ‘ADZABANAR</b><br/>
										Artinya:
											<i>" Ya Tuhan kami, berilah kami kebaikan di dunia dan kebaikan di akhirat, dan peliharalah kami dari siksa api neraka." </i><br/><br/>
									5. Sholat 2 rakaat di depan Maqom Ibrahim<br/>
										Maqom Ibrahim bukanlah kuburan dan tidak pula tempat yang terkait dengan kuburan lain. Namun di tempat itu Nabi Ibrahim pernah berdiri dalam rangka membangun Kabah. Rakaat pertama membaca surat Al-Fatihah dilanjutkan dengan membaca surat Al-Kaafiruun. Rakaat kedua membaca surat Al-Fatihah dilanjutkan dengan membaca surat Al-Ikhlas.<br/><br/>
									6. Beristirahat sejenak dan minum air zam-zam.<br/>
										Sebelum minum air zam-zam, membaca doa:<br/>
											<b>ALLAHUMMA INNI ASALUKA 'ILMAN NAFI'AN WA RISQON WAASI'AN WA SYIFAA'AN MIN KULLI DAA'IN WA SAQOMIN BI ROMHATIKA YA ARHAMAR ROHIMIIN<</b><br/>
										Artinya:<br/>
											<i>" Ya Allah, aku mohon padaMu ilmu pengetahuan yang bermanfaat, rezeki yang luas dan kesembuhan dari segala penyakit." </i><br/><br/>
									7. Melakukan sai antara Safa dan Marwah 7 kali bolak balik.<br/>
										Sai dimulai dari Safa ke Marwah yang dihitung sebagai satu kali perjalanan. Jadi, Safa ke Marwah 1, Marwah ke Safa 2, dan seterusnya. Sai berakhir di Marwah. Sai dikerjakan dengan berjalan, tapi pada batas di antara 2 lampu hijau, berlari-lari kecil.<br/>
										Sai ini merupakan penghargaan Allah SWT kepada istri Nabi Ibrahim. Saat itu istri Nabi Ibrahim, Siti Hajar, bolak-balik antara Safa dan Marwah sebanyak 7 kali dalam rangka mencari  air untuk minum putra beliau yaitu Nabi Ismail.<br/><br/>
									8. Melakukan tahallul<br/>
										Tahallul adalah akhir dari pelaksanaan ibadah umroh yang ditandai dengan bercukur. Untuk laki-laki lebih baik dicukur sampai gundul, tapi jika tidak sampai gundul tak mengapa. Sedangkan untuk tata cara umroh wanita hanya dicukur ala kadarnya.<br/>
										Dengan melakukan tahallul, maka sudah sempurna tata cara ibadah umroh lengkap sesuai sunnah.<br/>

								 </p>

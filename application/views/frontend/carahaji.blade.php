								<h3 class="about-title mb-30">Tata Cara Ibadah Haji yang Perlu Dipahami Sebelum Berangkat ke Tanah Suci</h3>
								<p>
									Haji merupakan salah satu kegiatan ibadah penting bagi umat Islam. Ibadah haji merupakan salah satu rukun Islam kelima,
									sekali seumur hidup. Ibadah yang dilakukan di Tanah Suci, Mekkah dan Madinah, ini menjadi paripurna dari keislaman seorang muslim.
									Ibadah haji mempunyai syarat-syarat tertentu. 
								 </p>
								<br/>
								<h3 class="about-title mb-30">Rukun Haji</h3>
								<p> 
									Rukun haji yaitu perbuatan yang wajib dlakukan dan tidak dapat diganti dengan membayar denda.
									Apabila seseorang meninggalkan satu rukun haji maka tidak sah hajinya. rukun haji ada 6 yaitu:<br/>
									1.  Ihram atau niat haji<br/>
									2.  Wukuf di padang Arafah<br/> 
									3.  Tawaf (keliling ka’bah)<br/>
									4.   Sa’I (berlari-lari kecil antara bukit safa dan marwah)<br/>
									5.  Menggunting atau mencukur rambut<br/>
									6.  Tertib<br/>
								 </p>
								 
								<h3 class="about-title mb-30">Wajib Haji</h3>
								<p> 
									Wajib haji yaitu perbuatan yang harus dikerjakan dalam ibadah haji. Apabila wajib haji dilanggar,
									maka hajinya tidak sah kecuali dengan membayar dam (denda) dengan cara menyembelih binatang. Wajib haji ada enam yaitu; <br/>
	 								1.  Ihram atau niat berhaji mulai dari miqat (batas yang ditentukan)<br/>
									2.  Mabit (bermalam) di Muzdalifah pada malam hari raya haji<br/>
									3.  Melontar tiga jumrah, yaitu : jumrah  ula, jumrah wusta, dan jumrah aqabah.<br/>
									4.  Mabit (bermalam) di Mina<br/>
									5.  Tawaf wada’ (tawaf perpisahan)<br/>
									6.  Menjauhkan diri dari larangan atau perbuatan yang diharamkan dalam ihram seperti:<br/>
										   a.  Bagi pria dilarang memakai pakaian berjahit.<br/>
										   b.  Menutup kepala bagi pria dan menutup muka bagi wanita<br/>
										   c. Membunuh hewan buruan.<br/>
										   d. Memotong kuku.<br/>
										   e. Memakai wangi-wangian.<br/>
										   f. Mengadakan aqad nikah.<br/>
										   g. Bersetubuh<br/>
										   h. Memotong rambut atau bulu badan yang lain.<br/>
								 </p>
								 
								 
								<h3 class="about-title mb-30">Tata cara ibadah haji</h3>
								<p> 
									1. Sebelum tanggal 8 Dzulhijjah, semua jamaah haji mulai untuk melaksanakan Tawaf Haji di Masjidil Haram (Makkah).<br/>
									2. Tanggal 8 Dzulhijjah (Hari Tarwiyah) disebut dengan hari tarwiyah, karena para jama’ah haji menyiapkan bekal secukupnya untuk menuju mina dan padang arafah, karena kedua tempat tersebut tidak ada sumber air.<br/>
									3. Jamaah haji melakukan ihram untuk ibadah haji, dimulai dengan mandi, memakai wewangian serta mengenakan pakaian ihram, sambil ber-talbiyah mengucapkan.<br/>
									4. Berangkat menuju Mina dan setelah di Mina, mereka mendirikan shalat zhuhur, ashar, maghrib dan isya serta shalat subuh. Setiap shalat dikerjakan pada waktunya, namun shalat yang jumlah rakaatnya empat diqashar sehingga menjadi dua rakaat. Para jamaah tetap berada di Mina sampai matahari terbit pada tanggal 9 Dzulhijjah.<br/>
									5. Tanggal 9 Dzulhijjah, pagi harinya semua jamaah haji menuju ke padang Arafah untuk melakukan wukuf.  Kemudian semua jamaah haji melaksanakan ibadah Wukuf, yaitu berdiam diri dan berdoa di padang Arafah hingga Maghrib tiba. Disunnahkan bagi jama’ah untuk singgah di namirah dan jika memungkinkan berdiam di sana hingga matahari tergelincir, jika memungkinkan.<br/>
										Namirah adalah sebuah tempat yang terletak dekat perbatasan arafah, apabila matahari tergelincir, dan masuk maktu zhuhur. Disunnahkan bagi imam atau orang yang diwakilkan untuk menyampaikan khutbah di hadapan para jama’ah, berkenaan dengan kondisi kaum muslimin, agar kembali memperbaharui tauhid, hukum-hukum seputar ibadah haji, dan perkara-perkara penting lainnya.<br/>
									6. Waktu wukuf di arafah mulai dari terbit fajar tanggal 9 dzulhijah hingga terbit fajar tanggal 10 dzulhijah. Barang siapa yang melakukan wukuf pada waktu tersebut walaupun sebentar, maka ia dianggap telah mengerjakan wukuf, dan hajinya sah.<br/>
										Barang siapa yang tidak mengerjakan wukuf pada waktu tersebut maka hajinya tidak sah, sebagaimana yang diriwayatkan dalam dari ibnu ‘abbas hadits marfu’ “barang siapa yang mengerjakan wukuf sebelum matahari terbit (pada tanggal 10 dzulhijjah) maka ia telah mengerjakan haji”. [Disahihkan oleh Al-Albani (No. 5995) dalam shahihul jami’.<br/>
									7. Tanggal 9 Dzulhijjah malam, semua jamaah haji menuju ke Muzdalifah untuk mabit (bermalam di muzdalifah) dan mengambil batu untuk melontar jumroh secukupnya.<br/>
									8. Tanggal 9 Dzulhijjah tengah malam atau  setelah melakukan mabit, jamaah haji meneruskan perjalanannya ke Mina untuk melaksanakan ibadah melontar Jumroh.<br/>
									9. Pada Tanggal 10 Dzulhijjah, jamaah haji melaksanakan ibadah melempar Jumroh yaitu sebanyak 7x  ke Jumrah Aqabah sebagai simbol untuk  mengusir setan. Dilanjutkan dengan tahalul yaitu mencukur rambut atau sebagian rambut.<br/>
									10. Jika jamaah mengambil nafar awal maka dapat dilanjutkan perjalanannya  ke Masjidil Haram untuk Tawaf Haji atau menyelesaikan Haji.<br/>
									11. Sedangkan jika mengambil nafar akhir, jamaah haji tetap tinggal di Mina dan dilanjutkan dengan melontar jumrah sambungan, yaitu jumrah ‘Ula dan jumrah Wustha.<br/>
									12. Tanggal 11 Dzulhijjah, jamaah haji melempar jumrah sambungan (Ula) di tugu pertama, tugu kedua, dan tugu ketiga.<br/>
									13. Tanggal 12 Dzulhijjah, jamaah haji melempar jumrah sambungan (wusta) di tugu pertama, tugu kedua, dan tugu ketiga.<br/>
									14. Kemudian yang terakhir Jamaah haji kembali ke Makkah untuk melaksanakan Tawaf Wada’ yaitu Tawaf perpisahan  sebelum pulang ke negara masing-masing.<br/>

